# -------------------------------------------------------------------------------
# ------------------- Variables -------------------
# -------------------------------------------------------------------------------
# Syntax => [Variable Name] [Assignment Operator] [Value]
# -------------------------------------------------------------------------------
# Name convention and rules
# [1] Can contain letters, digits, underscore(_).
# [2] Cannot contain special characters(!, @, #, $, %, ^, &, *, (, ), -).
# [3] Cannot start with digit or special characters. 
# [4] Case sensitive (Name not equal name).
# [5] Should be descriptive.
# -------------------------------------------------------------------------------

name = "Mohamed Ibrahim"
print(name)
print("-------------------------------------------------------------------------------")

# -------------------------------------------------------------------------------
# Variable naming styles (naming cases)
# [1] Snake case (Most common using in python).
# [2] Camel case.
# [3] Pascal case.
# [4] Upper case.
# -------------------------------------------------------------------------------

# Snake case.
user_name = "Mohamed"
total_price = 250

# Camel case.
userName = "Mohamed"
totalPrice = 250

# Pascal case.
UserName = "Mohamed"
TotalPrice = 250

# Upper case.
USERNAME = "Mohamed"
TOTALPRICE = 250

# help("keywords") => The reserved words in python 

a, b, c = 1, 2, 3

print(a)
