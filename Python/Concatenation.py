# -------------------------------------------------------------------------------
# ------------------------- Concatenation -------------------------
# -------------------------------------------------------------------------------
# - Concatenation => Add two strings together to make a larger one.
# - We can't concatenate string to anyother datatype, we just can concatenate 
# string to string. (String + String)
# -------------------------------------------------------------------------------

first_name = "Mohamed"
second_name = "Ibrahim"

print(first_name + second_name)
print(first_name +" "+ second_name)

full_name = first_name +" "+ second_name

print(full_name)

