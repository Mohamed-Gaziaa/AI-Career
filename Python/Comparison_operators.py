# -------------------------------------------------------------------------------
# -------------------------  Comparison operators -------------------------
# -------------------------------------------------------------------------------
# [ == ] Equal
# [ != ] Not equal
# [ > ] Greater than
# [ < ] Less than
# [ >= ] Greater than or equal
# [ <= ] Less than or equal
# -------------------------------------------------------------------------------

# Equal and not equal

print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print("#" * 50)

print(100 != 100)
print(100 != 200)
print(100 != 100.00)

print("#" * 50)

# Greater than and less than

print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 40)

print("#" * 50)

print(100 < 100)
print(100 < 200)
print(100 < 100.00)
print(100 < 40)

print("#" * 50)

# Greater than or equal and less than or equal

print(100 >= 100)
print(100 >= 200)
print(100 >= 100.00)
print(100 >= 40)

print("#" * 50)

print(100 <= 100)
print(100 <= 200)
print(100 <= 100.00)
print(100 <= 40)
