# -------- Why we use comments? --------
# -------------------------------------------------------------------------------
# 1) Write information about file.
# 2) Write license.
# 3) Who created the file.
# 4) When and Why the file created.
# 5) Prevent code from run.
# -------------------------------------------------------------------------------
